#BASIC MATH PRACTICE
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

#Addition
addition = num1 + num2
print(f"The Addition of number 1 & 2 is: {addition}")

#Subtraction
subtraction = num1 - num2
print(f"The Subtraction of number 1 & 2 is: {subtraction}")

#Division
division = num1 / num2
print(f"The Division of number 1 & 2 is: {round(division)}")

#Multiplication
multiplication = num1 * num2
print(f"The Multiplication of number 1 & 2 is: {multiplication}")